BLANK_PARAS = {445, 446, 447}


def rewrite(src: Path, dst: Path) -> None:
    with zipfile.ZipFile(src, "r") as zin:
        files = {name: zin.read(name) for name in zin.namelist()}
    root = ET.fromstring(files["word/document.xml"])
    paras = body_paragraphs(root)
    for idx, text in REWRITES.items():
        if idx < len(paras):
            replace_para_text_keep_style(paras[idx], text)
    for idx in BLANK_PARAS:
        if idx < len(paras):
            replace_para_text_keep_style(paras[idx], "")
    files["word/document.xml"] = ET.tostring(root, encoding="utf-8", xml_declaration=True)
    with zipfile.ZipFile(dst, "w", compression=zipfile.ZIP_DEFLATED) as zout:
        for name, data in files.items():
            zout.writestr(name, data)
    print(f"rewritten {len(REWRITES)} paragraphs; blanked {len(BLANK_PARAS)} summary paragraphs")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("src", type=Path)
    parser.add_argument("dst", type=Path)
    args = parser.parse_args()
    rewrite(args.src, args.dst)


if __name__ == "__main__":
    main()
